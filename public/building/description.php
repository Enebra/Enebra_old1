                <br/><br/>
                &emsp;In 2008, an unknown person or a group of people under the pseudonym Satoshi Nakomoto gave the world a decentralized crypto currency called Bitcoin (May 02, 2016 Australian entrepreneur Craig Wright has identified himself as Satoshi Nakamato, the pseudonymous creator of the digital currency Bitcoin.). The genius of this product was the fact that every member of a peer-to-peer network of equal users of Bitcoin holds information on all the transactions, ranging from the very first made in 2010 to the present day. All these transactions are recorded and stored in the blocks, and the blocks are arranged in a serial chain “blockchain”, the order of which cannot be changed due to the use of special hash functions. Each user of the Bitcoin system stores a complete copy of blockchain on his device, which makes it completely equal among all network participants. Today, there are 604 active crypto currencies created on the principle of similarity to Bitcoin, which once again proves the viability of the system. In five years of practical work of Bitcoin there became clear its advantages and drawbacks.
               
