                    <div class="h3">Introduction</div>
                
               <div>
                    &emsp;The birth of a human being on this Earth is a true miracle, bestowed by nature and located far beyond our understanding. At the time of his birth the first cry of a human being informs the whole world that he had come to this Earth and our land is ready for his appearance. Everything he needs is prepared for him: the air – to breathe, the breast milk and warmth – to eat and grow, the incredible beauty and variety of nature - to be developed and enjoyed, a crystal clear water – to drink. At the time of his birth, nature did not prepare his race, public or religious affiliation, social or political status, nature did not specify that he would be a slave or rule the world, all this and much more, mindfully, people made up for him. Nature so energized this little miracle with its positive energy, that being exhausted after birth, on having cuddled him, his mother feels so powerful rush of love and kindness, in the light of which, all the pain she's suffered, becomes just the flour dust.<br/><br/>
    &emsp; Nature freely awarded a newborn with his own uniqueness, which lies not only in the fingerprints, appearance, retina or DNA structure - it gave him a completely unique gift to think, feel, see and feel the world, thereby, complementing another drop that produces perfect balance. It suggested that the birth of this child creates a clear balance between the amount of resources on our planet and the number of people. The ecosystem of the land awarded a baby with 7 billionth part of its resources, which includes not only water and air, but also the iron ore, gold, oil and natural gas, granite, diamonds, forests, fields and deserts. Although, in contrast to the natural, the human system immediately withdrew from him all these gifts and gave them to the state, being somehow sure that nature has made a big mistake and the baby will not be able to dispose these gifts properly and that the use of these gifts will lead to the inevitable catastrophe. And as soon as the happy parents registered their child in the public authorities, thus, they declared him a citizen of his country. State care, compensating retrieved nature gifts, bestowed this little happy being, who has not yet had time to take the first step on this land, with the proportional part of its foreign debt. After all, today every country in the world has an external debt, for which every citizen of this country bears the responsibility. It is easy to calculate by dividing the amount of external debt on the citizens of this country. Every newborn American today has a debt in the amount of $ 62 398, Ukrainian - $ 4 609, Saud - $ 708, 
