    Я не считаю это проблемой, это дар! которым нас наградила природа и вместо того чтобы думать, как сократить население земли с использованием генетически модифицированной кукурузы, проектов по евгеники и стерилизации, войны и наркотиков необходимо думать, как освоить новые технологии и другие планеты. Вокруг нас необъятные вселенные обладающие колоссальными ресурсами и чем нас больше тем быстрее мы найдем ответы на интересующие нас вопросы тем больше величайших открытий нас ожидает и тем больше возможностей для нас открывается. Нам необходимо просто объединить наши знания и усилия в этом направлении. Если дать каждому человеку свободный и бесплатный доступ к знаниям и технологиям, возможность заниматься любимым делом, путешествовать по миру, участвовать в интересных проектах и полноценно развиваться, то я уверен, что со временем мы будем думать как стимулировать людей к воспроизведению себе подобных, так, как это сейчас происходит в развитых странах мира.
    <br /><br />
       <div class="h3">Образование</div>
       <br />
     &emsp; 
    Я не хочу углубляться в те удручающие факты, великолепно раскрытые в выше приведённых фильмах, открыто показывающих цели и состояние нашей системы образования призванной вырастить из нас послушное и управляемое стадо уверенно движущиеся в туннеле, построенном из тысяч стереотипов и правил без права даже усомниться в их истинности.


<div class="img-full"> 
    <div class="img-wrapper">
        <img class="desc-img" src="../img/desc/im17.png"/>
    </div>
