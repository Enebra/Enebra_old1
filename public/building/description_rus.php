        &emsp;
        
    Как только проект или технология занесена в blockchain, она становится доступной для всех участников сети Энебра. Если статус проекта <span class="span-med">«завершён»</span>, то он сразу размещается в системе для реализации конечным пользователям. Если статус проекта <span class="span-med">«открытая доработка»</span> каждый человек может принять участие в её доработке и если его доработки будут улучшать любой аспект данного проекта, и будут приняты участниками сети - тогда этот человек автоматически становится соавтором данного проекта. Для лучшего понимания данного механизма легче всего воспользоваться примером:<br/><br/>          
      
    
