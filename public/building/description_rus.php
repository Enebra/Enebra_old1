              </div> 
     &emsp;Природа постоянно показывает людям свою гениальность и сбалансированность всех нас. Ведь историей не зарегистрировано ни одного факта рождения человека со сверхъестественными способностями во всех направлениях, человека, который смог бы объединить в себе глаза Аристотеля – которому не нужен был полёт в космос чтобы определить что земля круглая, пальцами Людвига Вана Бетховина создавшими лунную сонату, будучи совершенно глухим, голосом Лучано Паваротти, мозгом Леонардо Да Винчи, возможностью провиденья Ванги, мимикой Чарльза Чаплина.
     <br/><br/>
     &emsp; Для того чтобы представить себе природную систему я взял её творение - уникальный саморегулирующийся организм человека. Тело и все органы человека состоят из клеток. В организме человека нет ни одной одинаковой клетки, как и в людском обществе, нет ни одного одинакового человека. Я представил себе человеческое тело, состоящее  не из клеток, а из каждого живого организма живущего на земле. В человеческом организме ежедневно отмирает и рождается огромное количество клеток, этот процесс можно сопоставить с рождением и смертью всего живого на нашей планете. При рождении новой клетки для неё уже подготовлено место и питательная среда, она не питается за счет соседних клеток, для неё созданы все условия для жизни и развития. Каждая клетка рождается со своей миссией и от качества её развития зависит качество развития всего организма.
     <br/><br/>
     &emsp;Но несмотря на все эти очевидные факты человечество продолжает жить по принципу разделяй и властвуй, используя при этом централизованные системы управления. В централизованной системе очень маленькой группе людей отведена роль сверхчеловека, решающих кому жить, а кому нет, кто будет жить в страхе и мучиться от того что ему есть что терять, а кто от того что ему нечего есть.
               
               
               
