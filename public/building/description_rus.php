&emsp;
    В мире существуют тысячи прогрессивных методик, позволяющих помочь человеку разносторонне развиться и найти именно свой неповторимый путь, где максимально будет раскрыт его талант и потенциал. Одну из таких методик почти 100 лет назад разработал австрийский философ Рудольф Штайнер на основе неё были созданы Вальдорфские школы принцип которых основан на том чтобы не мешать ребенку самостоятельно раскрыть свой потенциал. Но эти школы так и не смогли занять лидирующее положение в среде существующего государственного и частного образования т.к. руководству системы не нужны креативные и развитые подданные, гораздо проще управлять отсталыми и невежественными людьми. Да и сама цель обучения в современной системе сводиться к изучению того что способно принести вожделенные деньги, а не радость творчества. Задумайтесь над тем что самыми востребованными в современном мире стали юридические науки - позволяющие делать деньги на проблемах и страданиях людей и экономические - позволяющие создавать деньги из воздуха. Обидно осознавать, что десятилетия самого активного и ценного периода жизни, отданные получению знаний в государственных или частных учебных заведениях, не принесли и 5% тех знаний которыми мы ежедневно пользуемся и получаем в процессе жизни, самостоятельно по крупице собирая их из различных скрытых источников и опыта реальной жизни.
    <br/> <br/>  
&emsp;
    В системе «Энебра» все эти прогрессивные методики будут востребованы, так как изменится сама система ценностей. Все самые последние технологии, методики, научные открытия, данные со всех уголков земли и космоса будут находиться в открытом и бесплатном доступе. Нам лишь останется показать нашим детям весь спектр открытых перед ними возможностей и научить методике максимально простого получения необходимой им информации, а дальше просто не мешать их развитию, целью которого будет, не рабский труд во имя денег, а улучшение качества жизни людей и раскрытие их собственного потенциала.
    <br/> <br/>        
<div class="h3">
    Благотворительность

    


</div> 
<br />
&emsp;
    В настоящее время существуют десятки тысяч благотворительных, правозащитных, природоохранных организаций и фондов. Благодаря этим организациям были остановлены испытания ядерного оружия, остановлен промышленный отстрел китов, спасены от вырубки тысячи гектаров лесных массивов, именно благодаря этой ежедневной борьбе с правительствами и корпорациями, чьи интересы и представляют правительства, на нашей планете еще есть заповедники, национальные парки, остатки жизни в океанах и небольшое количество зон свободы слова, к которым пока ещё можно отнести интернет.


<div class="next-block"><a href="" class="next">Next</a></div>

        </td>
    <td class="equal-blocks">
   &emsp;
    Эти организации объединяют миллионы людей, желающих остановить уничтожение лесов, вымирание животных, найти эффективные методы лечения болезней и остановить разрушение озонового слоя земли. С одной стороны, эти люди ежедневно платят налоги в своих странах для содержания правительств, а с другой жертвуют своё время и делают денежные пожертвования на проведение природозащитных мероприятий и акций протеста, направленных против деятельности тех же правительств, для существование которых они платят налоги. Это по меньшей мере неразумно.


<br/> <br/>
   &emsp;
    В системе «Энебра» любой человек вместо потери времени на очередном митинге уже сегодня может принять участие в создании проекта Конституции планеты земля, <span class="span-med">для этого ненужно разрешение правительства</span>, просто напишите и обоснуйте свою норму и если большинство людей Вас поддержат, то эта норма станет неотъемлемой нормой жизни всех людей на земле.


<br/> <br/>
&emsp;
    Допустим можно внести норму, запрещающую убивать животных ради развлечения, и если она будет принята другими людьми, то в мире исчезнет такое понятие как охота ради развлечения или коррида, как в свое время исчезли гладиаторские бои. Для контроля соблюдения этой нормы, в дальнейшем, можно использовать ныне работающие на оборонную промышленность спутники шпионы, которые уже сегодня с высочайшим разрешением позволяют рассматривать песчинки на поверхности земли. Вся информация с этих спутников будет поступать в систему интернет и любой желающий сможет в любой момент оценить состояние земли и происходящие процессы не при помощи телевизора или статьи в газете, а при помощи визуального и программного анализа из первоисточника. На мой взгляд этот метод намного эффективнее ныне существующих о которых красноречиво рассказывает фильм Кип Андерсена «Cowspiracy: The Sustainability Secret» («Скотозаговор»).


<div class="img-full"> 
    <div class="img-wrapper">
        <img class="desc-img" src="../img/desc/im18.png"/>
    </div>
</div>

&emsp;
    Многие люди, добившиеся высокого уровня финансового достатка, тратят свои средства на помощь, по их мнению, нуждающимся, бедным слоям населения. В большинстве случаев большая часть этих средств оседает в карманах структур и исполнителей проведения этих акций. Попробую объяснить это на конкретном примере. Допустим некий филантроп проводит благотворительную акцию, направленную на борьбу с болезнью СПИД. Он передает средства в благотворительный фонд на закупку и бесплатную раздачу населению страны N средств контрацепции. Фонд проводит тендер среди предприятий производящих подобные продукты. За частую, победителем таких тендеров становятся предприятия возвращающие управлению благотворительного фонда части перечисленных средств в виде откатов. На оставшиеся деньги предприятие производит низкокачественные средства контрацепции, так как они всё равно предназначены не для продажи, и отправляет их организаторам проведения акции в страну N. В свою очередь организаторы подобных акций приглашают представителей СМИ, которые фиксируют бесплатную раздачу средств контрацепции населению что является доказательством для выделившего деньги филантропа. На Самом деле раздается не более 10% контрацептивов, а основная их часть расходиться по сетям аптек и реализуется за деньги. Подобные действия происходят при передаче секонд-хенда и т.д. В системе «Энебра» любой человек может заниматься благотворительностью, получая прозрачную и точную информацию относительно прохождения его средств и биометрических данных людей, получивших в руки его продукт.

<br/> <br/>
&emsp;
    При существующей системе все открытия и технологии появившееся за счет пожертвований скрыты от людей - в итоге становиться собственностью коммерческих структур и реализуются за деньги. В системе «Энебра» они полностью открыты и доступны любому человеку совершенно на бесплатной основе.  


<br/> <br/>
&emsp;
    В интерфейсе самого приложения «Энебра» существует кнопка «Рай» При её нажатии средства от человека поступают в часть «Финансирование мировых проектов» и люди совместно определяют на какие первоочередные нужды их направить. Для нажавшего на эту кнопку человека это означает что он более чем обеспечен, полностью само реализовался и отдает эти ресурсы для того чтобы тот, кто ещё не реализовал свои проекты смог улучшить качество жизни его и других людей на нашей планете. И когда большинство людей в системе воспользуются этой кнопкой на земле наступит <span class="span-med">процветание</span>.




