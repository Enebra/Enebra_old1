     <br/><br/>
    &emsp;   Второй значительный недостаток системы Bitcoin заключается в её, по сути, мнимой анонимности. Ведь как только любой участник системы пытается конвертировать свои накопления в любую фиатную валюту, он сталкивается либо с биржей, либо с банком или Bitcoin банкоматом, которые тут же фиксируют владельца цифрового кошелька. Или как только человек покупает любой товар или услугу за Bitcoin внутри системы он вынужден, для их получения, указать продавцу или гаранту свои физические реквизиты, что в свою очередь стирает всю анонимность. В течение жизни информация о любом человеке сохраняется в органах государственной власти при получении любого документа: свидетельства о рождении, паспорта, водительского удостоверения. Информация о вас и ваших болезнях разрозненно хранится во всех лечебных учреждениях, которые вы посещали. Информация о ваших доходах и сбережениях хранится в банках, в отделах кадров и у кассиров вашего предприятия. Вы оставляете свои данные в нотариальных конторах, посольствах при получении визы, а если взять пример посольства США, то и свои биометрические данные. Мы пользуемся электронной почтой на централизованных серверах, общаемся в централизованных социальных сетях, в которых даже если мы прячем свое истинное имя, открыто показываем всех наших друзей или коллег, которые знают про нас практически всё. Этот список можно очень долго продолжать. В результате этого появляется справедливый и закономерный вопрос, от кого и зачем мы так неэффективно прячемся и зачем мы тратим самое дорогое, что есть у человека – время, на постоянное заполнение одних и тех же личных данных в громадной бюрократической машине.
     <br/><br/>
    &emsp;   В системе Bitcoin электронный кошелёк имеет два цифровых ключа – публичный (открытый), который по сути является адресом, на который участнику сети поступает крипто валюта, и приватный, который необходимо надёжно спрятать от чужих глаз, ибо только при его помощи можно осуществить платёж со своего кошелька. Несмотря на то, что математические и криптографические функции надежно защищают систему Bitcoin, никто не может гарантировать, что ваш публичный и приватный ключ не будут украдены с вашего компьютера любым злоумышленником через локальную или интернет сеть, с использованием уязвимостей вашей операционной системы, не говоря о банальной физической утрате самого девайса.
     <br/><br/>
    &emsp;  По этой причине в системе «Энебра» приватный ключ заменён на <span class="span-med">биометрические данные человека</span>, что в свою очередь полностью исключает возможность потери данных при любых обстоятельствах. 
    <br/><br/>
    &emsp; В сегодняшнем мире существует сложно разрешимая проблема, которая заключается в использовании биометрических данных человека под давлением извне, или под страхом потери жизни. Но в проекте «Энебра» все пользователи аутентифицированы, и любая транзакция может быть сделана только между пользователями сети. По этой причине становится совершенно бесполезным применение насилия ведь все транзакции навечно сохраняются в блоках, блоки выстроены в blockchain, а копии blockchain сохранены на всех девайсах пользователей системы. Этот фактор резко снизит уровень преступности во всем мире.
     <br/><br/>
     &emsp;       В отличие от существующих децентрализованных систем, система «Энебра» сохраняет в блоке любые, внесенные в определённом формате данные, а не только данные о передаче ресурсного эквивалента. Это делает возможным полностью уйти от бюрократии и в процессе жизни, лишь добавлять приобретенные личные данные к тем, что уже занесены в систему при регистрации. В системе каждый человек сам принимает решение, какие данные открыть для публичного пользования, а какие сохранять в зашифрованном состоянии. Схожая система сохранения личных данных уже разработана и успешно применяется в Литве.
