                    <div class="h3">Введение</div>
                
               <div>
                    &emsp;Рождение человека на этой земле — это истинное чудо, дарованное природой и находящееся далеко за пределами нашего понимания. В момент своего рождения своим первым криком человек оповещает весь мир о том, что он прибыл на эту землю и наша земля готова к его появлению. Она приготовила для него всё необходимое: воздух – для того что бы он дышал, материнское молоко и тепло – что бы он питался и рос, невероятную красоту и разнообразие природы – для того чтобы он развивался и наслаждался, кристально чистую воду – для того что бы он пил. В момент его рождения природа не готовила ему расовую, государственную или религиозную принадлежность, социальный или политический статус, природа не определяла, быть ему рабом или властвовать над миром, всё это и многое другое, предусмотрительно, придумали для него люди. Природа настолько зарядила это маленькое чудо своей положительной энергией, что измученная родами мать, прижав его к себе, чувствует настолько мощный прилив добра и любви, в свете которого всё перенесённые ею муки становиться просто пылью.
    <br/><br/>
    
    &emsp; 
    Природа бесплатно наградила новорождённого, присущей только ему неповторимостью, заключающуюся не только в отпечатках пальцев, внешностью, сетчаткой глаза или составом ДНК - она одарила его совершенно неповторимым даром мыслить, чувствовать, видеть и ощущать окружающий мир дополнив тем самым еще одну каплю для создаваемого ею идеального баланса. Она посчитала, что рождение этого ребёнка создает четкий баланс между количеством ресурсов на нашей планете и количеством людей. Экосистема земли наградила младенца 7 миллиардной частью своих ресурсов, в которую входит не только вода и воздух, но и как не странно железная руда, золото, нефть и газ, гранит и алмазы, леса, поля и пустыни. Но в отличие от природной, человеческая система тут же отобрала у него все эти дары и передала их государству, будучи почему-то уверенной, что природа сделала большую ошибку и ребёнок не сможет распорядиться этими дарами правильно и, что использование этих даров ребёнком приведёт к неизбежной катастрофе. И как только счастливые родители зарегистрировали своё дитя в органах государственной власти, тем самым они объявили его гражданином своей страны. Государство заботливо, компенсируя забранные природой дары, одарило этого маленького счастливого человечка, ещё не успевшего сделать первый шаг по этой земле, пропорциональной частью своего внешнего долга. Ведь сегодня каждая страна в этом мире имеет внешний долг, за который в ответе каждый гражданин этой страны. Это легко посчитать, разделив внешний долг на количество граждан этой страны. Каждый новорождённый американец сегодня должен примерно $ 62 398, украинец - $ 4 609, 
саудит - $ 708, японец - $ 98 025 т.д.
              <div class="img-full"> 
