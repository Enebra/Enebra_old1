                     <div class="h3">Section 2 <span>Citizen's Rights, Freedoms and Duties </span></div>
                
                <ul>
                    <li><span>Article 1:</span>&emsp;All Citizens are free and equal in dignity and rights. The rights and freedoms of a Citizen are inalienable and inviolable; </li>
                    <li><span>Article 2:</span>&emsp;Rights and freedoms of a person and a Citizen, specified by this Constitution, considered being not exhaustive. The constitutional rights and freedoms are guaranteed and cannot be abolished. When adopting new laws or amending existing laws, there are not allowed narrowing of the content and scope of the existing rights and freedoms;</li>
                    <li><span>Article 3:</span>&emsp;Every Citizen has the right to freedom of personal development and self-realization, if it does not violate the rights and freedoms of others; and hereby, a Citizen has duties in reference to other Citizens, which will ensure the free and full development of his personality;</li>
                    <li><span>Article 4:</span>
    &emsp;All Citizens have equal constitutional rights and freedoms and are equal before the law. There can be no privileges or restrictions based on race, skin color, political, religious and other beliefs, sex, ethnic or social origin, property status, place of residence, language or other characteristics;
</li>
                    <li><span>Article 5:</span>
    &emsp;Everyone has the inherent right to life. No one shall be arbitrarily deprived of his life. The duty of a Citizen is to protect human life. Everyone has the right to protect his life and health, life and health of other persons against unlawful encroachments;
</li>
                    <li><span>Article 6:</span>                 
    &emsp;Everyone has the right to respect of his dignity. No one shall be subjected to torture or to cruel, inhuman or disparaging treatment or punishment. Not a single person, without a voluntary consent, subjected to medical, scientific or other experiments;
</li>

                    <li><span>Article 7:</span>&emsp;Everyone is guaranteed privacy of correspondence, telephone conversations or other communications;</li>
                    <li><span>Article 8:</span>&emsp;No one shall be subjected to interference with his privacy and family;</li>
                    
                    <li><span>Article 9:</span>
    &emsp;Marriage is based on the free consent of a woman and a man. Each of the spouses shall have equal rights and responsibilities in marriage and family;
</li>
                    
                    <li><span>Article 10:</span>&emsp;Everyone shall be guaranteed the right to freedom of thought and speech, free expression of views and beliefs. Everyone has the right to freely collect, store, use and disseminate information by the oral, written or other means in accordance with his choice;</li>
                    <li><span>Article 11:</span>&emsp;Everyone has the right to freedom of personal philosophy and religion;</li>
                    <li><span>Article 12:</span>&emsp;Everyone is guaranteed freedom of movement and free choice of residence;</li>
                    
                    <li><span>Article 13:</span>
    &emsp;Everyone has the right to possess, use and dispose his property and the results of his intellectual and creative activity. No one can be unlawfully deprived of the property rights. The right to private property is inviolable. The use of property may not cause harm to the rights, freedoms and dignity of other Citizens, the interests of society, aggravate the ecological situation and the natural qualities of the land;
</li>
                    <li><span>Article 14:</span>&emsp;Everyone has the right to work that includes the opportunity to improve the quality of life by his occupation, which he freely chooses or to which freely agrees;</li>
                    
                    <li><span>Article 15:</span>&emsp;Everyone has the right to housing. No one can be involuntary deprived of housing;</li>
                    <li><span>Article 16:</span>&emsp;Everyone has the right to an adequate standard of living for himself and his family, including adequate food, clothing and housing;</li>
                    <li><span>Article 17:</span>&emsp;Everyone has the right to the environment secure for his life and health. Everyone is guaranteed the right of free accesses to the environmental information, the quality of food and household items, as well as the right to its dissemination. The information cannot be classified;
</li>
                </ul>
                              
