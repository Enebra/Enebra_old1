                    <div class="h3">Section 1 <span>Earth Constitution General Provisions</span></div>
                
                <ul>
                    <li><span>Article 1:</span>&emsp;The Earth is a common heritage, which is indivisible and equally belongs to all living people; </li>
                    <li><span>Article 2:</span>&emsp;A person, his life and health, honor and dignity, inviolability and security are recognized as the highest social value on the Earth;</li>
                    <li><span>Article 3:</span>&emsp;On the Earth everyone can have the only citizenship. Anyone under any circumstances cannot be deprived of this citizenship. From the moment of birth to the moment of death any person has the status of a Citizen of the Earth;</li>
                    <li><span>Article 4:</span>&emsp;The power of the Citizens of the Earth is based on a decentralized management system of humanity, which is a manifestation of higher degree of equality and genuine democracy. All power on the Earth belongs to its Citizens. Since the age of majority every Citizen equally bears the responsibility and conducts the legislative, executive and judicial power. A Citizen shall exercise power directly and personally through the Internet system by open vote, using approved programs with the open source software;</li>
                    <li><span>Article 5:</span>&emsp;The Earth, its resources, air, water and other natural resources, equally stated to be the property of all Citizens of the world and should be used economically, efficiently and for the benefit of all Citizens and future generations, taking into account the minimum negative consequences for the ecosystem of the planet;</li>
                    <li><span>Article 6:</span>&emsp;The basis of the economic system on the Earth is a source-based economy, as the most effective and contributive to the development of human civilization, scientific and technological progress, preservation and management of the natural resources and minerals of the planet. It contributes to the conservation, augmentation and evolutionary development of flora and fauna of its biological diversity, and the development of an ecosystem of the Earth as a whole;</li>
                    <li><span>Article 7:</span>&emsp;In order to simplify the trade flow with a view to monitoring and restoration of the natural resources on the Earth – there introduced the unified resource equivalent - enebra. Emission and calculation of enebra and its compliance with the number of actual resources of the planet is made automatically on the basis of constantly updated data and software with the open source law approved by the Citizens of the Earth in the following manner and under this Constitution;</li>
   
                </ul>
                              
