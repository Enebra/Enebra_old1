                     <ul>
                    <li><span>Article 18:</span>
    &emsp;Parents must take care and have a right to dispose of the funds of their child for the personal benefit of a child under the age of majority. Adult children are obliged to care for and have the right to dispose of the funds of their disabled parents only for the personal benefit of their parents;
</li>
                    <li><span>Article 19:</span>
    &emsp;All existing knowledge, technologies and scientific discoveries are opened, accessible and equally belong to all citizens freely;
</li>
                    <li><span>Article 20:</span>
    &emsp;Children are equal in their rights regardless of their origin and whether they are born within or out of marriage. Any violence and child exploitation are unacceptable, immoral and punishable by law;
</li>
                    <li><span>Article 21:</span>
    &emsp;Everyone has an equal right to education and self-education, and unhindered access to any information, training and treatment, scientific and technical innovations and discoveries, direct real-time access to the data obtained from the sources of underground, ground-based, water, underwater, tropospheric and space kind;
</li>
                    <li><span>Article 22:</span>
    &emsp;The rights and freedoms of a person and a Citizen are protected by the unified civil court. Everyone has the right to protect his rights and freedoms from violations and illegal encroachments by any means not prohibited by law;
</li>
                    <li><span>Article 23:</span>
   &emsp; Everyone shall be guaranteed the right to know his rights and responsibilities. The laws and other normative - legal acts that determine the rights and obligations of the Citizens should be in constant unimpeded access to all Citizens. Every Citizen is obliged to know, directly participate in making and strictly abide by the adopted legislation;
</li>
                    <li><span>Article 24:</span>
    &emsp;The laws and other normative - legal acts have no retroactive force, except in cases where they mitigate or annul the responsibility of a person. No one can be responsible for the acts that at the time of commission were not recognized by the law as an offense;
</li>
                    <li><span>Article 25:</span>
    &emsp;No one can be prosecuted twice for one and the same offense. Responsibility of a Citizen implies an individual character;
</li>
                    <li><span>Article 26:</span>
    &emsp;A Citizen is presumed innocent of committing a crime and cannot be punished until his guilt is proved through the legal procedure and established by a verdict of guilt by not less than two-thirds of Citizens, who participated in the court session. Nobody is obliged to prove his innocence of committing a crime. The prosecution cannot be based on the evidence obtained by illegal means, as well as on assumptions. All doubts in concern with the guilt of a Citizen should be interpreted in his favor. In the event of an acquittal verdict, a Citizen, acting as a prosecutor, is responsible before the law for defamation;
</li>
                    
                    <li><span>Article 27:</span>
   &emsp; A Citizen does not bear responsibility for refusing to testify or to explain anything about his family members or close relatives as determined by the law. A convicted person enjoys all the rights of a person and a Citizen with the exception of restrictions determined by the law and established by the court verdict;
</li>
                    <li><span>Article 28:</span>
    &emsp;Everyone is obliged not to harm the nature or the cultural heritage and to compensate the damage incurred by him in accordance with the legislation;
</li>
                    
                    <li><span>Article 29:</span>
    &emsp;Every Citizen shall assume an equal share of responsibility for the conservation and humane treatment of the animal diversity on the Earth. Torture and deprivation of life of animals for entertainment is immoral and prohibited in all the forms and manifestations;
</li>
                    <li><span>Article 30:</span>
    &emsp;Everyone is obliged to strictly abide by the Earth Constitution, laws and other legal acts and do not infringe on the rights and freedoms, honor and dignity of other Citizens.
Ignorance of the law is a direct violation of the Earth Constitution;</li>
            
            </ul>
