                    <li><span>Article 8:</span>&emsp;Every Citizen has an unimpeded access to the open source code by which the emissions of enebra produced, knows the data on the number of all issued enebra and has a full list of transactions and their sequence performed on using enebra. Enebra cannot be given to credit or debt, transferred to someone as a gift or inheritance;</li>
                    <li><span>Article 9:</span>&emsp;The Earth and its orbit are declared the area of a stable world. On the Earth we prohibit the development, testing, storage and use of weapons of mass destruction in all its forms. It concerns with any kind of weapons - underground, ground-based, air, water, underwater, tropospheric, space – the one of a defensive or offensive action, including the ammunition designed for military operations;</li>
                    <li><span>Article 10:</span>&emsp;A comprehensive identification and authentication of a Citizen is a complex of his biometric characteristics obtained and stored in accordance with the accepted standards and the list of approved hardware;</li>
                    <li><span>Article 11:</span>&emsp;All the available knowledge, technologies and scientific discoveries are open, accessible and belong equally to all Citizens free of charge;</li>
                    <li><span>Article 12:</span>&emsp;The copyright and co-authorship rights of a Citizen are inviolable. A Citizen reserves the copyright and co-authorship rights at the time of publication of an innovation in accordance with the procedure of the established rules and standards. Every Citizen has the right of remuneration for the innovation, copyright and co-authorship rights, which belong to him. Calculation of compensation is done in accordance with the approved rules and directly proportional to the number of Citizens, using this innovation;</li>
                    <li><span>Article 13:</span>&emsp;The system of the Internet is a free world heritage that cannot be censored, controlled by anyone, managed or persecuted so that can only be limited by the moral principles accepted by all Citizens and enshrined in the basic law;</li>
                    <li><span>Article 14:</span>&emsp;All Citizens of the Earth are exonerated from the forced payment of any taxes and fees. Any Citizen may voluntarily participate in charity and raise funds for his personal consent;</li>
                    <li><span>Article 15:</span>&emsp;Every Citizen has the right to have equal social security and to carry out indispensable rights for his dignity and free development of his personality in the economic, social and cultural fields from his birth to the moment of death in accordance with the resource potential of the Earth and the level of technical development of society;</li>
                    </ul>
