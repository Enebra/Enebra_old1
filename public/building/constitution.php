            <div class="h2">EARTH CONSTITUTION (DRAFT)<span>Preamble</span></div>
            &emsp;We are now living free people of the Earth by our own will proclaim this Constitution in the name of conservation, development and prosperity of the human civilization, combining knowledge and technology to improve the quality of life for all people, the preservation of fauna and natural diversity, rational and economical use of natural resources of the entire planet's ecosystem, the abolition of the destructive processes that inhibit the evolutionary development of mankind. Herein, we assure our will with open voting on the Internet, using software with open source approved by us and using our personal biometric data. Each of us takes an equal share of responsibility to his conscience, to each other and to future generations for our decisions and the provisions of this Constitution.
    </div>
