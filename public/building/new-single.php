                            <h1 class="span-med">INCREASING THE POWER OF ALTERNATIVE ENERGY SOURCES</h1>
          &emsp;The PV revolution is based on a high-tech but remarkably simple technology that converts sunlight directly to electricity (Figure 1). Photons of light striking certain materials used in PV panels cause electrons to be released, and when the panels are connected to a circuit, those electrons provide power for the full range of our electricity needs. Solar panels involve no moving parts, no fuel other than the sun, and no other inputs or by-products.
          Solar is viable in virtually every part of the country (Figure 2, p. 4). In a sunny location such as Los Angeles or Phoenix, a five-kilowatt residential system produces an average of 7,000 to 8,000 kilowatt-hours per year, roughly equivalent to the electricity usage of a typical U.S. household. And solar achieves similar results in many other parts of the country as well. In northern climates such as that in Portland, ME, that same system on average generates 85 percent of what it would in Los Angeles, and 95 percent of what it would in Miami. The system in Maine would generate 6 percent 
          more electricity than in Houston (NREL 2014a).
            <br/><br/>
        &emsp;Small-scale PV is often referred to as “rooftop” because it is most often installed on residential or commercial roofs, but these systems can be installed on other structures such as parking lots or on the ground. In most cases, these systems are considered a form of “distributed” generation because they are connected to the distribution grid of poles and wires that transport electricity from substations. On-site electricity needs are first supplied by the electricity generated by the PV system, with the grid supplying electricity when the sun is not shining. When the home or business generates more electricity than it consumes, the electricity is fed back into the grid. 
        <br/><br/>
        &emsp;<span class="span-med">Integrating rooftop solar into the grid.</span>The transition to an electricity system with a much larger amount of distributed solar provides many benefits to the grid as well as the environment, but it also presents some challenges for utilities and grid operators. One of the biggest benefits that distributed solar provides to the grid is that it often produces electricity when—and where—that power is most valuable. In many regions, demands on the electricity system peak in the afternoon on hot, sunny days, when air conditioning demands are high and when rooftop solar is performing strongly. Such systems therefore help utilities meet peak demand without firing up seldom-used but expensive and more-polluting power plants fueled by oil or natural gas (Burger 2011). Rooftop systems also reduce strain on electricity distribution and transmission equipment by allowing homes and 
        <div class="next-block"><a href="" class="next">Next</a></div>                    
                            </td>
                            <td class="">
                            businesses to first draw power on-site instead of relying completely on the electricity grid. The benefits are twofold: the use of on-site power avoids the inefficiencies of transporting electricity over long distances, and on-site systems potentially allow the utility to postpone expensive upgrades to its infrastructure (Bird et al. 2013).    

        &emsp;However, having power flowing fromcustomers, instead of to them, is a relatively new situation for utilities. Neighborhoods where many homes have adopted solar can approach a point at which the rooftop systems can produce more than the neighborhood can use during the day. Yet “feeder” lines that serve such neighborhoods may not be ready to handle flows of electricity in the opposite direction. Several locales around the United States, including Atlantic City, San Diego, and the Hawaiian island of Oahu, have areas approaching that point, potentially causing problems for additional home owners looking to adopt solar. More broadly, the variability of solar generation presents new challenges 
because grid operators cannot control the output of these systems with the flip of a switch like they can with many non-
renewable power plants.
<br/><br/>
      &emsp;But the issues associated with adding more rooftop PV to the grid are eminently solvable. Fixes to the feeder issue are largely economic, not technical; modifications can be made to existing systems to allow reverse electricity flows, and the primary hurdle is who pays. The variability challenges are well understood in part because grid operators already manage fluctuations caused by constantly changing electricity demand and drops in electricity supplies when large power plants or transmission lines unexpectedly fail. Much of the variability inherent in solar generation is also predictable and manageable. Hours of daylight and seasonal changes are highly predictable, and weather forecasts can help grid operators plan for when cloud cover may hinder electricity 
generation from rooftop solar systems. Variations caused by local cloud cover can be managed by incorporating many systems across a wide geographic area.
<br/><br/>
       &emsp;Low levels of renewable energy generation can typically be integrated into the grid at negligible costs, but higher levels will likely require other generators on the system to operate more flexibly, and investments in grid flexibility devices like energy storage, which can help make use of solar power even when the sun is not shining (Bird el al. 2013). Many locales are already successfully accommodating high rates of solar adoption. On the island of Oahu, for example, 1 in 10 utility customers has solar (Wesoff 2014). And studies suggest that it is possible to integrate significantly higher levels of variable renewable energy (solar and wind) into the grid using existing technologies (Mai et al. 2012).

    <h3>THE INCREASINGLY ATTRACTIVE ECONOMICS OF ROOFTOP SOLAR</h3>

      &emsp;Rooftop solar is increasingly cost-effective for home owners, 
business owners, and their communities, thanks to reductions in technology prices, innovative financing, and growing networks of solar installers and financial partners (Figure 3). Prices for household systems in the United States fell by 29 percent from 2010 to 2013, from an average of $32,000 for a five-kilowatt system to under $23,000, before tax credits or other incentives (GTM Research and SEIA 2014a). The federal solar investment tax credit returns 30 percent of that purchase price, and state and local tax credits, rebates, and other support in leading states can then cut the total cost to under $10,000 (Internal Revenue Code 2011; see also, for example, Massachusetts 2014).
<br/><br/>
Dropping prices are due to economies of scale and technological advances.The falling price of rooftop PV systems results from improvements in the technology and economies of scale among manufacturers. Global solar panel production (for rooftop and other markets) increased from 24,000 MW in 2010 to 40,000 MW in 2013 (Mehta 2014). PV costs in the United States are also affected by global market conditions, including the emergence of lower-priced solar products from China.
                            
                            
                            
                            
