    &emsp;Although, I no longer think of this to be a question but more of an excuse for the people who are either afraid or do not want to do anything. Nevertheless, I will try as much as possible to answer it.
    <br/><br/>
    &emsp;To fully answer this question, it is necessary to understand the structure and function of the governments, corporations and the dominant clans. If you look under the veil of these names, you will find there common people, visually no different from all the others.
     <br/><br/>
    &emsp;To begin with, it is the power of politicians and top officials. Each of these people has passed a thorny career road. At times, most of these people did the dirty work, sometimes compromising their principles, while getting rather an average salary, which in no way could be their main motivator. The main target was their desired career, which they paid dearly for. To participate in the party, they had to pay the party dues, wasted their family time on the public events, and collected compromising information concerning their competitors or superiors. And in the final dash to the peak of their career they had to pass a complicated lottery in the form of elections, which in the current system is not possible without money. If you count the cost of air time, newspaper articles, printed materials, billboards, mass events and fundraisers, you get the amount that only several lifetimes are enough to make. For this reason, a politician or an official is forced to look for support from big businesses, and if he/she is lucky enough to find a sponsor, there still remains the electoral process itself, which is hard to influence by legal means.
 <br/><br/>
    &emsp;If a person fails, it's a fall into debt slavery for the rest of one's life, and if wins, there is a necessity lobby the interests of sponsors. As a result, a considerable chunk of one's life and health is spent to achieve the coveted chair, sitting in which the human is called “the authority". But there is also a constant stress of paying back the sponsors on the one hand, and on the other continuously fending off pretenders willing to occupy the position and the necessity to get compensated for the funds, health and efforts invested while using the advantage of the official post. And even that's not the end of the story: as soon as the one starts abusing the position of power because legal means are never sufficient to cover the politician's debts, the bankers will immediately get the means of control, being able to immediately nullify or freeze the illegally acquired money in case of political or other disagreements. 
 <br/><br/>
    &emsp;Try to imagine that after earning and accumulating wealth for the long time, taking and paying back loans to build a house, largely limiting own needs and wishes, and after the goal is achieved, someone opens the door wide and invites everyone to live in this house for free, only for the reason that not everyone has a house like this. I am more than sure that for most of people such a course of action is shocking and generally unacceptable. People build the houses for themselves and their families, not for some strangers. 
 <br/><br/>
    &emsp;To cut it short, there is little difference between the person sitting in a chair of “power” and “authority” and anyone else. First of all this person, like most of the human beings, will take care of and create some comfort for the self family, secondly will pay those, who helped to gain the coveted seat, and thirdly get the environment of more or less trusted accomplices in order to save the achieved position as long as possible. There will be simply no time to fulfill the promises to the electorate, who, after being fed the information paid for by then-candidate, have sacrificed an hour of their time to put a dash next to the name in the electoral bulletin and believe for no apparent reason that the elected one owes them something.
 <br/><br/>
    &emsp;But in reality such an official will have much better opportunity to realize the personal potential in the Enebra system than in any other, since all possible levers of pressure disappear and the one can really go for creating the legislation aimed at improving the quality of life and giving moral satisfaction and lifetime fee.
 <br/><br/>
    &emsp;I am not an idealist, and I understand that not all senior officials simply agree to lose their positions to which they devoted a significant part of their lives, but their relationship with the rest of the world's population is negligible and their hopes for the law enforcement agencies are useless, as the army and police are composed of the ordinary people, who would live in a much more favorable Enebra system than become pariahs for their people while following the officials' orders and obeying them in the hope for improved wages or some negligible benefits.

