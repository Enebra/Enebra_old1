    &emsp;Now let’s have a closer look at such entities as corporations. The main goal of any corporation is to obtain the maximum profit. The enemy of any corporation is a competition that forces to reduce production costs in the struggle for resources and the market. For this reason the corporations appear as small businesses trying to compete successfully merge or are absorbed by the larger companies. The victory in the competition is a monopoly, that's why corporations so generously fund the promotion of their officials to the higher state administration.
  <br/><br/>
    &emsp;Monopoly achieves maximum profit, because it can set any price and degrade the quality of products without fear of losing the market and does not need to waste a lot of money on advertising, moreover, it can completely automate the enterprise, increasing revenue through layoffs.
  <br/><br/>
    &emsp;In the beginning of the technological revolution the competition has played a very positive role and became a strong catalyst for the scientific and technological progress, but in the modern world, being merged with the politics, it has exhausted its positive potential and became a progress-stopper in both technical and social spheres, uselessly devouring the very limited resources of our planet. The beneficiaries of the corporations as well as of the administration are the ordinary people looking for resources for themselves and their families. The shareholders of the top corporations profit at the expense of those, who daily develop, manufacture and market production. Every employee of a corporation does not receive more than 10% of the personally earned money, 15% are earned by the shareholders, the rest is eaten by taxes, enterprise development, advertising and overproduction. 
  <br/><br/>
    &emsp;As in the previous example with politicians the ratio of shareholders to employees is negligible. In the Enebra system the people will be engaged in the development or production and will receive 100% of the benefits they earned. This will lead to a natural end of corporations as the scientists, engineers and workers find it much more profitable to work individually rather than for the benefit of the company owners. The shareholders might be trying to stop this process with the help of the authorities, though, as I have described above, it is a dead-end course.
  <br/><br/>
    &emsp;Now, let’s consider the privileged classes. They are comprised, oddly enough, of exactly the same human beings. By some estimates, they can fit into one Boeing. Their main superiority and control lever is a printing press, which gives birth to the blood of the modern economy - money. At their disposal there is a large amount of land and the natural resources. Though it is useless without those who actually produce real goods; the land is useless if no one cultivates it; the palaces, belonging to them, now only exist because thousands of people serve them every day. Imagine the Queen of England with a mop in her hands and 775 rooms of the Buckingham Palace, which is only a small part of her residences.
  <br/><br/>
    &emsp;Furthermore, none of even the richest people on the Earth can guarantee themselves, their families and children safety - their life might be ceased if someone presses a nuclear button for some reason, or simply there happens an absurd technical or software failure.
  <br/><br/>
    &emsp;In the Enebra project we have the opportunity to permanently remove the danger of use of any weapons of mass destruction; people are given a generator of the exchange units (resource equivalents) which is much more honest and predictable then current printing press, bearing no interest. As a result, the elite will have to exchange it's resources for necessary goods and services without any violence. All that is not related to the natural resources has no inherent value, as the constructions, capital and goods tend to become outdated and require constant maintenance and updates. In reality, people with the large capital will live much easier in the Enebra system, which eliminates the need to protect their capital and captured position so that the quality of life will be many times greater in comparison to what they could achieve in the current system.
  <br/><br/>
    &emsp;Based on the above, I would like to conclude that in the moment of transition from one system to another there may be minor confrontations, but only so long as the old system will be maintained. The main obstacle would be people themselves, those who a nurtured with a fear of taking responsibility for their own lives. As a result, a new system cannot be stopped, for it is more efficient and progressive; it's just an evolutionary process of replacing the obsolete with the new.
                          
            
