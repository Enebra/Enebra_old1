                                <div class="text-center">Сезоны года и жизнь растений</div>

Соотношение длины дня и ночи имеет огромное значение в жизни комнатных растений. Питательные вещества вырабатываются растениями только в течении светлого времени суток, а расходуются они непрырывно — на дыхание и транспирацию, а также и на прирост. Поэтому, чем длиннее день, тем больше получают растения питательных веществ, а чем длиннее ночь, тем больше их расход. Я представлю общие рекомендации для растений, узнать индивидуальные особенности конкретного растения можно в каталоге комнатных растений (цветов) в приложенном файле.
    Так же в приложенном файле вы найдете разработанный мной график и перечень работ по уходу за комнатными растениями расположенными на нашей лестничной клетке. Прошу доработать его с учетом ваших пожеланий.

Зима
Декабрь — самый темный месяц в году. Дни убывают вплоть до зимнего солнцестояния, и вместе с тем непрерывно ухудшаются условия существования комнатных растений, живущих в основном за счет отложенных летом запасов питательных веществ, т.к. дневная выработка этих веществ ничтожно мала. Зацветают камелии, и по прежнему цветут цикламены. 
                            
